package object;

public class User
{
	private String u_id;
	private String u_name;
	private int u_type;
	private int u_school_id ;
	private int u_grade ;
	private int u_class ;
	public String getU_id()
	{
		return u_id;
	}
	public void setU_id(String uId)
	{
		u_id = uId;
	}
	public String getU_name()
	{
		return u_name;
	}
	public void setU_name(String uName)
	{
		u_name = uName;
	}
	public int getU_type()
	{
		return u_type;
	}
	public void setU_type(int uType)
	{
		u_type = uType;
	}
	public int getU_school_id()
	{
		return u_school_id;
	}
	public void setU_school_id(int uSchoolId)
	{
		u_school_id = uSchoolId;
	}
	public int getU_grade()
	{
		return u_grade;
	}
	public void setU_grade(int uGrade)
	{
		u_grade = uGrade;
	}
	public int getU_class()
	{
		return u_class;
	}
	public void setU_class(int uClass)
	{
		u_class = uClass;
	}

}
